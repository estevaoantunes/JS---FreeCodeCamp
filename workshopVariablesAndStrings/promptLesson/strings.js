//What Is String Concatenation, and How Can You Concatenate Strings with Variables?

let firstName = "John";
let lastName = "Doe";

let fullName = firstName + lastName;

console.log(fullName); 

let greeting = "Hello";

greeting += ', John!';

console.log(greeting);

let str1 = 'Hello';
let str2 = 'World';

let result = str1.concat(' ', str2);
console.log(result);

